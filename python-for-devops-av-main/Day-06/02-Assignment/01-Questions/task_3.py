## Task 3: Logical Operators

#1. Create two boolean variables, `x` and `y`.
#2. Use logical operators (`and`, `or`, `not`) to perform various logical operations on `x` and `y`.
#3. Print the results.

x = True
y = False

and_xy = x and y
or_xy = x or y
not_x = not x
not_y = not y

print("x and y :", and_xy)
print("x or y :", or_xy)
print("not x :", not_x)
print("not y :", not_y)
