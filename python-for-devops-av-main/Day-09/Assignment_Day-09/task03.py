# `break` Statement

#The "break" statement is used to exit the loop prematurely. It can be applied to both "for" and 
#    "while" loops, allowing you to terminate the loop when a particular condition is met.
#Example:
numbers = [1, 2, 3, 4, 5, 6]
for number in numbers:
    if number == 4:
        break
    print(number)

